VOID	PUT_CHAR_FD(CHAR CHARACTER, INT FD)
{
	WRITE(FD, &CHARACTER, 1);
}

VOID	PUT_CHAR(CHAR CHARACTER)
{
	PUT_CHAR_FD(CHARACTER, 1);
}
